#include "3-calc.h"
/**
 * op_add - add 2 intgers
 * @a: the 1 int
 * @b: the 2 int
 * Return: summ
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - to sub 2 intgers
 * @a: the 1 int
 * @b: the 2 int
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - to multi 2 intgers
 * @a: the 1 int
 * @b: the 2 int
 * Return: mult
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of 2 intgers
 * @a: the 1 int
 * @b: the 2 int
 * Return: divion
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo 2 intgers
 * @a: the 1 int
 * @b: the 2 int
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
